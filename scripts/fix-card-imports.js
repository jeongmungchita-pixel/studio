#!/usr/bin/env node

const fs = require('fs');
const path = require('path');
const glob = require('glob');

// í†µê³„
let totalFiles = 0;
let modifiedFiles = 0;
let totalFixes = 0;

function fixCardImports(content) {
  let modifiedContent = content;
  let fixCount = 0;

  // Fix import statements
  const importPatterns = [
    { search: /_CardHeader/g, replace: 'CardHeader' },
    { search: /_CardTitle/g, replace: 'CardTitle' },
  ];

  importPatterns.forEach(({ search, replace }) => {
    const matches = modifiedContent.match(search);
    if (matches) {
      fixCount += matches.length;
      modifiedContent = modifiedContent.replace(search, replace);
    }
  });

  return { content: modifiedContent, count: fixCount };
}

// íŒŒì¼ ì²˜ë¦¬
function processFile(filePath) {
  try {
    const content = fs.readFileSync(filePath, 'utf8');
    const { content: modifiedContent, count } = fixCardImports(content);
    
    if (content !== modifiedContent) {
      fs.writeFileSync(filePath, modifiedContent, 'utf8');
      modifiedFiles++;
      totalFixes += count;
      console.log(`âœ… Fixed: ${filePath} (${count} changes)`);
    }
    
    totalFiles++;
  } catch (error) {
    console.error(`âŒ Error processing ${filePath}:`, error.message);
  }
}

// ë©”ì¸ ì‹¤í–‰
function main() {
  console.log('ğŸ”§ Fixing Card component imports...\n');

  const patterns = [
    'src/**/*.{ts,tsx}',
  ];

  let allFiles = [];
  patterns.forEach(pattern => {
    const files = glob.sync(pattern, { 
      ignore: ['**/node_modules/**', '**/.next/**', '**/dist/**'],
      nodir: true 
    });
    allFiles = allFiles.concat(files);
  });

  // ì¤‘ë³µ ì œê±°
  allFiles = [...new Set(allFiles)];

  console.log(`ğŸ“ Found ${allFiles.length} files to check\n`);

  // ê° íŒŒì¼ ì²˜ë¦¬
  allFiles.forEach(processFile);

  // ê²°ê³¼ ì¶œë ¥
  console.log('\n' + '='.repeat(60));
  console.log('ğŸ“Š FIX SUMMARY');
  console.log('='.repeat(60));
  console.log(`Total files scanned: ${totalFiles}`);
  console.log(`Files modified: ${modifiedFiles}`);
  console.log(`Import issues fixed: ${totalFixes}`);
  console.log('\nâœ¨ Import fix complete!');
}

// ì‹¤í–‰
main();
